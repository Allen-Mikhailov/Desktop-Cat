void flipfont(char array[27][25])
{
    //Flipping the array
    for (int a = 0; a < 27; a++)
    {
        for (int y = 0; y < 2; y++)
        {
            for (int x = 0; x < 5; x++)
            {
                char t = array[a][y*5+x];
                array[a][y*5+x] = array[a][(4-y)*5+x];
                array[a][(4-y)*5 + x] = t;
                // array[a][y] = 0;
            }
        }
    }
}

void presentfont(char array[27][25])
{
    for (int i = 0; i < 27; i++)
    {
        printf("\nChar : %i \n", i);
        for (int y = 0; y < 5; y++)
        {
            for (int x = 0; x < 5; x++)
            {
                printf("%i, ", array[i][y*5+x]);
            }
            printf("\n");
        }
    }
}